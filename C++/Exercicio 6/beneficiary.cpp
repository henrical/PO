#include "beneficiary.h"


void Beneficiary::initFunds()
{
	funds = 0;
}

int Beneficiary::getSupportLevel() const
{
	return supportLevel;
}

void Beneficiary::setSupportLevel(int n)
{
	supportLevel = n;
}

